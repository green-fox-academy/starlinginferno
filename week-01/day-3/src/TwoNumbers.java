public class TwoNumbers {
    public static void main(String[] args) {

        int a = 22;
        int b = 13;

        System.out.println(("22 + 13 = ") + (a + b));
        System.out.println(("22 - 13 = ") + (a - b));
        System.out.println(("22 * 13 = ") + (a * b));
        System.out.println(("22 / 13 = ") + ((double)a / b));
        System.out.println(("(int) 22 / 13 = ") + (a / b));
        System.out.println(("remainder of 22 / 13 = ") + (a % b));



    }


}
